const { Subcategory } = require('./models');
const { sequelize, Op } = require('sequelize');

async function fixImageUrlFormat() {
  try {
    console.log('üîç Fixing image URL format in subcategories...');

    // Get all subcategories with featureImage URLs
    const subcategoriesWithImages = await Subcategory.findAll({
      where: {
        featureImage: {
          [Op.ne]: null,
          [Op.ne]: '',
          [Op.like]: '/api/storage/images/%' // Only fix relative API paths
        }
      }
    });

    console.log(`üìù Found ${subcategoriesWithImages.length} subcategories with relative API image URLs`);

    if (subcategoriesWithImages.length === 0) {
      console.log('‚úÖ No subcategories need URL format fixes');
      return;
    }

    const API_BASE_URL = process.env.NODE_ENV === 'production'
      ? 'https://menastories.me'
      : 'http://localhost:5000';

    let fixedCount = 0;
    let errors = [];

    // Fix each subcategory
    for (const subcategory of subcategoriesWithImages) {
      try {
        const currentUrl = subcategory.featureImage;
        console.log(`\nüîç Processing subcategory: ${subcategory.name}`);
        console.log(`   Current URL: ${currentUrl}`);

        // Convert relative API path to full URL
        if (currentUrl.startsWith('/api/storage/images/')) {
          const newUrl = `${API_BASE_URL}${currentUrl}`;
          await subcategory.update({ featureImage: newUrl });
          console.log(`   ‚úÖ Fixed: ${newUrl}`);
          fixedCount++;
        } else {
          console.log(`   ‚ÑπÔ∏è URL format is already correct`);
        }

      } catch (error) {
        console.error(`   ‚ùå Error processing subcategory ${subcategory.name}:`, error.message);
        errors.push({ subcategory: subcategory.name, error: error.message });
      }
    }

    // Summary
    console.log('\n' + '='.repeat(50));
    console.log('üìä IMAGE URL FORMAT FIX SUMMARY');
    console.log('='.repeat(50));
    console.log(`‚úÖ Fixed subcategories: ${fixedCount}`);
    console.log(`‚ùå Errors encountered: ${errors.length}`);

    if (errors.length > 0) {
      console.log('\n‚ùå Errors:');
      errors.forEach((error, index) => {
        console.log(`   ${index + 1}. ${error.subcategory}: ${error.error}`);
      });
    }

    // Show final URLs
    console.log('\nüìã Final subcategory image URLs:');
    const finalSubcategories = await Subcategory.findAll({
      where: {
        featureImage: {
          [Op.ne]: null,
          [Op.ne]: ''
        }
      }
    });

    finalSubcategories.forEach(sub => {
      console.log(`   ${sub.name}: ${sub.featureImage}`);
    });

    console.log('\n‚úÖ Image URL format fix completed successfully');

  } catch (error) {
    console.error('‚ùå Error fixing image URL format:', error);
  } finally {
    process.exit(0);
  }
}

// Run the fix
fixImageUrlFormat();